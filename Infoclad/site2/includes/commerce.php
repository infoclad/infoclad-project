				<p align="justify">
O estoque do produto acabado, matéria-prima e material em processo não serão vistos como independentes. Todas as decisões tomadas sobre um dos tipos de estoque influenciarão os outros tipos. Às vezes acabam se esquecendo dessa regra nas estruturas de organização mais tradicionais e conservadoras. O controle de estoque tem também o objetivo de planejar, controlar e replanejar o material armazenado na empresa.

				</p>
				<br/>
				<h1><strong>Características:</strong></h1>
				<br/>
				<p>
Para se organizar um setor de controle de estoque, inicialmente deveremos descrever suas principais funções:
<br/><br/>
a) Determinar o que deve permanecer em estoque. Número de itens;<br/>
b) Determinar quando se deve reabastecer o estoque. Prioridade;<br/>
c) Determinar a quantidade de estoque que será necessário para um período pré-determinado;<br/>
d) Acionar o departamento de compras para executar a aquisição de estoque;<br/>
e) Receber, armazenar e atender os materiais estocados de acordo com as necessidades;<br/>
f) Controlar o estoque em termos de quantidade e valor e fornecer informações sobre sua posição;<br/>
g) Manter inventários periódicos para avaliação das quantidades e estados dos materiais estocados;<br/>
h) Identificar e retirar do estoque os itens danificados.<br/>
<br/><br/>
Existem determinados aspectos que devem ser especificados, antes de se montar um sistema de controle de estoque. Um deles refere-se aos diferentes tipos de estoques existentes em uma fábrica. Os principais tipos encontrados em uma empresa industrial são: matéria-prima, produto em processo, produto acabado e peças de manutenção. 				</p>
