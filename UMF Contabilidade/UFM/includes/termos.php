<p align="justify" class="sigla"><span style="color: #336699"><span><strong>BALANÇO PATRIMONIAL</strong></span></span><span style="color: #336699"><strong><br>
</strong></span>Relatório pelo qual é possível mensurar a situação patrimonial e financeira de uma entidade.<br>
<br>
<span style="color: #336699"><strong>DEMONSTRAÇÃO DE RESULTADOS</strong></span><br>
Relatório utilizado para se verificar as causas de alterações na situação patrimonial líquida de uma entidade (lucro ou prejuízo).<br>
<br>
<span style="color: #336699"><strong>DOAR</strong></span><span style="color: #336699"><strong><br>
</strong></span>Demonstração das origens e aplicações de recursos. Indica as fontes de recursos e a forma como estes foram aplicados.<br>
<br>
<span style="color: #336699"><strong>ESTOURO DE CAIXA</strong></span><span style="color: #336699"><strong><br>
</strong></span>Termo utilizado para indicar que os gastos previstos (ou realizados) são superiores a capacidade de pagamento imediato.<br>
<br>
<span style="color: #336699"><strong>MUTAÇÃO DO PATRIMÔNIO LÍQUIDO</strong></span><span style="color: #336699"><strong><br>
</strong></span>Demonstrativo que indica as causas de variações ocorridas no patrimônio líquido de uma entidade (lucros, prejuízos, aumento ou diminuição de capital, entre outros).<br>
<span style="color: #336699"><strong><br>
EQUIVALÊNCIA PATRIMONIAL</strong></span><span style="color: #336699"><strong><br>
</strong></span>Método pelo qual se apura o resultado relativo ao investimento feito numa sociedade controlada. Este procedimento leva em consideração o patrimônio líquido da investida em relação ao custo inicial sendo que, a medida que o "PL" da investida sofre "mutação", esta é refletida nos resgistros da investidora.<br>
<br>
<strong><span style="color: #336699">ATIVO CIRCULANTE</span></strong><strong><span style="color: #336699"><br>
</span></strong>É o conjunto de bens e direitos cuja realização é inferior a 360 dias (regra básica).<br>
<br>
<span style="color: #336699"><strong>PASSIVO CIRCULANTE</strong></span><span style="color: #336699"><strong><br>
</strong></span>É o conjunto de obrigações cuja exigibilidade é inferior a 360 dias (regra básica).<br>
<br>
<strong><span style="color: #336699">PATRIMÔNIO LÍQUIDO</span></strong><strong><span style="color: #336699"><br>
</span></strong>É o conjunto de bens, direitos e obrigações de uma entidade.<br>
<br>
<span style="color: #336699"><strong>CMV</strong></span><span style="color: #336699"><strong><br>
</strong></span>Custo da Mercadoria Vendida. Equação básica: Estoque inicial + Compras - Estoque final.<br>
<br>
<span style="color: #336699"><strong>LONGO PRAZO</strong></span><span style="color: #336699"><strong><br>
</strong></span>Todo e qualquer direito (ativo) ou obrigação (passivo) cuja realização esteja prevista para após 360 dias da data de levantamento do balanço patrimonial.<br>
<br>
<span style="color: #336699"><strong>DIFERIDO</strong></span><span style="color: #336699"><strong><br>
</strong></span>Conceito básico: todos os valores (ativos ou passivos) que se transformam em despesas ou receitas em decorrência do regime de competência, condições contratuais, entre outras situações.<br>
<br>
<span style="color: #336699"><strong>FATO CONTÁBIL</strong></span><span style="color: #336699"><strong><br>
</strong></span>São eventos que, direta ou indiretamente, podem modificar a situação patrimonial de uma entidade.<br>
<br>
<span style="color: #336699"><strong>INDEDUTÍVEL</strong></span><span style="color: #336699"><strong><br>
</strong></span>São despesas que não são aceitas para efeito de redução da base de cálculo dos impostos incidentes sobre o lucro.<br>
<br>
<span style="color: #336699"><strong>LUCRO REAL</strong></span><span style="color: #336699"><strong><br>
</strong></span>É o lucro contábil ajustado segundo as normas previstas pela legislação federal, seja pela adição de despesas indedutíveis ou pela exclusão de receitas isentas.<br>
<br>
<span style="color: #336699"><strong>LUCRO PRESUMIDO</strong></span><span style="color: #336699"><strong><br>
</strong></span>Metodologia pela qual aplica-se um determinado percentual sobre o faturamento para que, então, se conheça a base de cálculo dos impostos federais (IRPJ e CS Lucro).<br>
<br>
<span style="color: #336699"><strong>PARTIDA DOBRADA</strong></span><span style="color: #336699"><strong><br>
</strong></span>Método pelo qual se orienta a contabilidade e que exige a seguinte condição: "para cada débito deverá existir um crédito correspondente, ou seja: a soma dos débitos deve ser igual a soma dos créditos".<br>
<br>
<span style="color: #336699"><strong>REGIME DE COMPETÊNCIA</strong></span><span style="color: #336699"><strong><br>
</strong></span>Forma de contabilização que considera como data-base o momento da ocorrência do fato e não sua realização financeira.<br>
<br>
<span style="color: #336699"><strong>REGIME DE CAIXA</strong></span><span style="color: #336699"><strong><br>
</strong></span>Forma de contabilização que considera como data-base o recebimento ou pagamento de um determinado valor, independente do momento em que os direitos ou obrigações foram gerados.</p> 	 
