						<h2 class="grayText">Pessoa Física - Malha Fina</h2>
						<span class="b p5"><!--  --></span>
						<p class="grayText">A Secretaria da Receita Federal, por meio de sistemas próprios, detecta, automaticamente, falhas nas declarações do imposto de renda da pessoa física e estas declarações são "separadas" para análise/esclarecimentos. Este processo é conhecido como "malha fina".
						
						<br>
O bom conhecimento, aliado a métodos adequados de trabalho, são elementos essenciais a correta entrega de tão importante documento.<br>
<br>
<strong>Alguns cuidados básicos:</strong><br>
<br>
<strong>a)</strong> sempre que efetuar um gasto, exija e guarde adequadamente o comprovante que necessariamente deve trazer o nome completo à quem o pagamento foi feito, a natureza e valor, bem como o número do CPF ou CNPJ do mesmo (boletos bancários sem a indicação clara do beneficiário pode dificultar a recuperação de impostos).<br>
<br>
<strong>b)</strong> seus gastos não podem ultrapassar o total de seus rendimentos (considerar rendimento toda e qualquer fonte de valores, incluindo empréstimos e contas especiais junto as instituições financeiras);<br>
<br>
<strong>c)</strong> documentar, de modo claro e preciso, toda e qualquer transação de compra e venda de bens móveis e imóveis - lembre-se do ganho de capital e da necessidade de existência de capacidade financeira para saldar a compra.<br>
<br>
<strong>d)</strong> o regulamento do imposto de renda determina que todos os pagamentos efetuados sejam declarados.<br>
<br>
<strong>e) </strong>mantenha de modo organizado todos os recebimentos ocorridos (aluguel, pro-labore, lucros, salários, etc) - estes itens montam a sua base de recolhimento de impostos e também podem possibilitar a recuperação de valores retidos.<br>
<br>
<strong>f)</strong> também por sistemas próprios, a Receita Federal mantém controle de diversos valores movimentados ao longo do ano, incluindo: cpmf, cartões de crédito, empréstimos, imposto retido na fonte - assim, lembre-se que todo documento deve instruir a sua declaraçao, sob pena de questionamento.


