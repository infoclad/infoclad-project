	<h2 class="grayText">Agenda de Obrigações</h2>
	<span class="b p5"><!--  --></span>
	<span class="b">

<u><span style="color: #336699">Siglas comuns utilizadas nas áreas contábil e fiscal.<br>
<br>
</span></u></h4>
<p style="text-align: left"><strong><span class="sigla">IRPJ:</span></strong> Imposto de Renda Pessoa Jurídica. É o imposto incidente sobre o lucro auferido pela pessoa jurídica.&nbsp;<br>
<br>
&nbsp;</p>
<p style="text-align: left"><strong><span class="sigla">IRPF:</span> </strong>Imposto de Renda Pessoa Física. É o imposto incidente sobre os rendimentos auferidos pela pessoa física.<br>
&nbsp;<br>
&nbsp;</p>
<p style="text-align: left"><strong><span class="sigla">PIS:</span></strong> Programa de Integração Social. Comumente chamada de "Contribuição ao Pis", com várias modalidades de recolhimento.<font color="#808080">&nbsp;</font><br>
&nbsp;</p>
<p style="text-align: left"><strong><span class="sigla">COFINS:</span></strong> Contribuição para Financiamento da Seguridade Social. Apresenta várias formas de cálculo/pagamento.<br>
&nbsp;</p>
<p style="text-align: left"><strong><span class="sigla">ISS:</span> </strong>Imposto Sobre Serviços. Imposto de responsabilidade municipal incidente sobre o valor dos serviços prestados.<br>
&nbsp;</p>
<p style="text-align: left"><strong><span class="sigla">IPI:</span></strong> Imposto sobre Produtos Industrializados. É devido pelas indústrias, importadores e equiparados.<br>
&nbsp;</p>
<p style="text-align: left"><strong><span class="sigla">ICMS:</span></strong> Imposto sobre Circulação de Mercadorias e Serviços de Transporte, Comunicação e Energia Elétrica.<br>
&nbsp;</p>
<p style="text-align: left"><strong><span class="sigla">CSL:</span> </strong>Contribuição Social Sobre o Lucro. Contribuição incidente sobre o lucro da pessoa jurídica.<br>
&nbsp;</p>
<p style="text-align: left"><strong><span class="sigla">CNPJ:</span> </strong>Cadastro Nacional da Pessoa Jurídica. É mantido pela Secretaria da Receita Federal.<br>
&nbsp;</p>
<p style="text-align: left"><strong><span class="sigla">I.E.:</span> </strong>Incrição Estadual. É o número de cadastro junto a Secretaria da Fazenda Estadual.<br>
&nbsp;</p>
<p style="text-align: left"><strong><span class="sigla">TFA:</span></strong> Taxa de Fiscalização de Anúncios. É cobrada anualmente pelas Prefeituras.<br>
&nbsp;</p>
<p style="text-align: left"><strong><span class="sigla">CADAN:</span></strong> Cadastro de Anúncios. Cadastro pelo qual todos anúncios efetuados devem ser informados à Prefeitura.<br>
&nbsp;</p>
<p style="text-align: left"><strong><span class="sigla">IN SRF:</span></strong> Instrução Normativa da Secretaria da Receita Federal. Atos editados/publicados pela Receita Federal no sentido de normatizar procedimentos relativos ao cumprimento de dispositivos legais.<br>
&nbsp;</p>
<p style="text-align: left"><strong><span class="sigla">DCTF:</span></strong> Declaração de Contribuições e Tributos Federais. Entregue trimestralmente, contempla todos os impostos federais devidos pelas pessoas jurídicas.<br>
&nbsp;</p>
<p style="text-align: left"><strong><span class="sigla">DACON:</span> </strong>Demonstrativo Acumulado - Pis e Cofins. Similar a DCTF, contempla exclusivamente os valores devidos relativos ao Pis e Cofins não cumulativos.<br>
&nbsp;</p>
<p style="text-align: left"><strong><span class="sigla">DIRF:</span> </strong>Declaração de Imposto Retido na Fonte. Apresentação anual, informa à Receita Federal todos os impostos retidos de terceiros.<br>
&nbsp;</p>
<p style="text-align: left"><strong><span class="sigla">DARF:</span></strong> Documento de Arrecadação de Receitas Federais. Guia utilizada para pagamento de impostos e contribuições federais.<br>
&nbsp;</p>
<p style="text-align: left"><strong><span class="sigla">CAGED:</span></strong> Cadastro Geral de Empregados e Desempregados. Formulário entregue mensalmente, pelo qual se indica a movimentação de contratações e desligamentos de <br>
funcionários.<br>
&nbsp;</p>
<p style="text-align: left"><strong><span class="sigla">JUCESP:</span> </strong>Junta Comercial do Estado de São Paulo. Órgão responsável pelo registro de instrumentos contratuais e demais atos societários, além de outros documentos sujeitos a registro.<br>
&nbsp;</p>
<p style="text-align: left"><strong><span class="sigla">GIA ICMS:</span></strong> Guia de Informação e Apuração do ICMS.<br>
&nbsp;<br>
&nbsp;</p>
<p style="text-align: left"><strong><span class="sigla">CCM:</span> </strong>Cadastro de Contribuintes Mobiliários. Número de registro junto a Prefeitura de São Paulo.<br>
<br>
&nbsp;</p>
<p style="text-align: left"><strong><span class="sigla">CIPA:</span> </strong>Comissão Interna de Prevenção de Acidentes.<br>
&nbsp;</p>
<p style="text-align: left"><strong><span class="sigla">DIPJ:</span> </strong>Declaração de Imposto de Renda Pessoa Jurídica.<br>
<br>
&nbsp;</p>
<p style="text-align: left"><strong><span class="sigla">DARM:</span></strong> Documento de Arrecadação de Receitas Municipais.<br>
&nbsp;<br>
&nbsp;</p>
<p style="text-align: left"><strong><span class="sigla">LALUR:</span> </strong>Livro de Apuração do Lucro Real.&nbsp;</p> 
	 
	</p>
