		<p>
			<h2 class="blueText">A Pastoral</h2>
			<br/>
			É uma iniciativa social e evangelizadora da igreja católica. Onde a igreja à luz do evangelho dialoga com a comunidade, a fim de descobrir e fomentar a prática esportiva e de lazer que mais se adequa a sua realidade.
			<br/><br/>
			<h2 class="blueText">Objetivos da pastoral</h2>
			<br/>
			<ul>
				<li>Anunciar a boa nova;</li>
				<li>Resgate da cidadania;</li>
				<li>Abordar o esporte de participação;</li>
				<li>Planejar, organizar, executar e participar de eventos esportivos e de lazer, de abrangência: de Vicariatos, de Dioceses nacionais e internacionais.</li>
			</ul>
			<br/>
			<h2 class="blueText">Como surgiu?</h2>
			<br/>
			As vésperas das Olimpíadas de  2004, o Papa João PauloII, que foi esportista na juventude,  criou na igreja o Departamento do Esporte, hoje conhecido como Pastoral do Esporte.
			<br/><br/>
			<h2 class="blueText">A Pastoral no Rio de Janeiro</h2>
			<br/>
Em sua essência, a                 
                                        Pastoral do Esporte    
                                        no Estado do Rio                       
                                         de Janeiro já  acontecia   a mais de 20 anos, através de iniciativas do, Pe. Lino na Paróquia Cristo Operário, em Vila Kennedy. As Olimpíadas da Paz realizadas em 2008, foi mais um elemento importante para dar corpo a Pastoral do Esporte no Rio de Janeiro.
			<br/><br/>
			<h2 class="blueText">Fundação Pastoral</h2>
			<br/>
Em novembro de 2009, durante o encontro com o Arcebispo do Rio, Dom Orani João Tempesta, no Vicariato Oeste, foi nomeado como Coordenador Geral da 
   Pastoral do Esporte da 
   Diocese do Estado do Rio  
   de Janeiro o Pe. Lino.
			<br/><br/>
			<h2 class="blueText">Composição da Pastoral</h2>
			<br/>
			<ul>
			<li>A Pastoral do Esporte em sua dinâmica social é composta por:</li>
			<li>Coordenador Geral da Pastoral (Pe. Lino);</li>
			<li>Coordenador da Pastoral por Vicariatos;( Padres)</li>
			<li>Conselheiros;( Prof. E. Física)</li>
			<li>Participantes(moradores da comunidade).</li>
			</ul>
			<br/><br/>
			<h2 class="blueText">Como trabalhamos</h2>
			<br/>
			<ul>
			<li>Através de uma formação sócio-esportiva</li>
			<li>Implantação de núcleos de esporte e de lazer nas paróquias;</li>
			<li>Conferência anual;</li>
			<li>Organizando eventos esportivos e de lazer (competições, passeios, retiros e confraternizações).</li>
			<li>&nbsp;</li>
			<li>Com a formação religiosaa:</li>
			<li>Através de uma formação bíblica, onde os grupos que de forma semelhante aos círculos bíblicos, vão refletir semanalmente sobre a leitura do evangelho do domingo e sobre a vida.</li>
			</ul>
			<br/><br/>
			<h2 class="blueText">Missão catequética</h2>
			<br/>
			João Paulo II nos diz que o departamento do esporte deve ser referência da igreja  para as organizações nacionais e internacionais; deve sensibilizar as igrejas locais a respeito da atenção pastoral dos  ambientes  esportivos;
favorecer uma cultura do esporte que promova a atividade esportiva e de lazer como meio para o desenvolvimento integral da pessoa, e como meio para a paz e a fraternidade entre os povos; organizar e sustentar iniciativas que ajudam a suscitar testemunhos de vida cristã entre os esportistas, como por exemplo levar mensagens de paz para os espaços esportivos de forma a conter as brigas de torcidas e o doping.
			<br/><br/>
			<h2 class="blueText">Missão pedagógica</h2>
			<br/>
			<ul>
			<li>Propor aos Conselheiros e aos Coordenadores dos Vicariatos o estudo de temáticas específicas referidas ao esporte e ao lazer.</li>
			<li>Planejar, utilizar, organizar todas as ações realizadas nas dinâmicas de aula e de eventos.</li>
			</ul>
